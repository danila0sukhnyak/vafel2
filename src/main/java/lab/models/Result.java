package lab.models;

public class Result {
    double x;
    long time;
    double iter;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public double getIter() {
        return iter;
    }

    public void setIter(double iter) {
        this.iter = iter;
    }
}
